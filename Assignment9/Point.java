package com.metacube.get2019;

/**
 * It is a point class used to represent a point.
 * @author Rivak shah
 *
 */
public class Point {
	public int x, y;
	
	Point() {}
	
	/**
	 * It is a constructor used to intialize point class attributes.
	 * @param x x coordinate of point
	 * @param y y coordinate of point
	 */
	public Point( int x, int y) {
		this.x = x;
		this.y = y;
	}

}
